task autonomous(){
	runAutonomousForTeamColor(roundColor);
	if(skills){
		if(roundColor == MHTeamColorRed || roundColor == MHTeamColorBlue){
			int wallSide = 1;
			if(roundColor == MHTeamColorBlue){
				wallSide *= -1;
			}
			int skyriseBaseSide = -wallSide;
			//Grab the third skyrise
			SensorValue[skyriseClaw] = MHPneumaticPositionClosed;
			wait1Msec(MHTimeOneSecond);
			//Place the skyrise
			liftForEncoderDistance(MHSkyriseThreeSkyrises, MHMotorPowerMax);
			liftCubeForTime(MHTimeHalfSecond, MHLiftDirectionDown);
			motor[skyriseArm] = MHMotorPowerHalf * skyriseBaseSide;
			wait1Msec(MHTimeOneSecond);
			resetLift();
			wait1Msec(MHTimeOneSecond);
			SensorValue[skyriseClaw] = MHPneumaticPositionOpen;
			//Grab the next skyrise
			motor[skyriseArm] = MHMotorPowerHalf * wallSide;
			wait1Msec(MHTimeOneSecond);
			motor[skyriseArm] = MHMotorPowerStop;
			wait1Msec(MHTimeOneSecond);
			SensorValue[skyriseClaw] = MHPneumaticPositionClosed;
			//Place the skyrise
			liftForEncoderDistance(MHSkyriseFourSkyrises, MHMotorPowerMax);
			motor[skyriseArm] = MHMotorPowerHalf * skyriseBaseSide;
			wait1Msec(MHTimeOneSecond);
			liftForEncoderDistance(MHSkyriseLiftInaccuracy, -MHMotorPowerMax);
			SensorValue[skyriseClaw] = MHPneumaticPositionOpen;
			//Reset the skyrise arm
			motor[skyriseArm] = MHMotorPowerMax * wallSide;
			wait1Msec(MHTimeOneSecond);
			motor[skyriseArm] = MHMotorPowerStop;
			//Grab the next skyrise
			motor[skyriseArm] = MHMotorPowerHalf * wallSide;
			wait1Msec(MHTimeOneSecond);
			motor[skyriseArm] = MHMotorPowerStop;
			wait1Msec(MHTimeOneSecond);
			SensorValue[skyriseClaw] = MHPneumaticPositionClosed;
			//Place the skyrise
			liftForEncoderDistance(MHSkyriseFiveSkyrises, MHMotorPowerMax);
			motor[skyriseArm] = MHMotorPowerHalf * skyriseBaseSide;
			wait1Msec(MHTimeOneSecond);
			liftForEncoderDistance(MHSkyriseLiftInaccuracy, -MHMotorPowerMax);
			SensorValue[skyriseClaw] = MHPneumaticPositionOpen;
			//Reset the skyrise arm
			motor[skyriseArm] = MHMotorPowerMax * wallSide;
			wait1Msec(MHTimeOneSecond);
			motor[skyriseArm] = MHMotorPowerStop;
		}
	}
	basicDrive(MHMotorPowerMax, MHMotorPowerMax);
	wait1Msec(MHTimeHalfSecond);
	stopDrive();
}
task usercontrol(){
	//Drive control
	bLCDBacklight = true;
	string first;
	string second;
	while(true){
		if(nMotorEncoder[lbLift] < 0){
			resetEncoders();
		}
		if(nLCDButtons != MHLCDButtonNone){
			resetEncoders();
		}
		sprintf(first, "%d", nMotorEncoder[lbLift]);
		sprintf(second, "%d", SensorValue[armAngle]);
		//displayLCDCenteredString(0, first);
		//displayLCDCenteredString(1, second);
		if(abs(vexRT[Ch2]) <= 30){
			stopDriveSide(MHRobotSideRight);
		}
		else{
			motor[rDrive] = vexRT[Ch2];
		}
		if(abs(vexRT[Ch3]) <= 30){
			stopDriveSide(MHRobotSideLeft);
		}
		else{
			motor[lmDrive] = motor[lbDrive] = vexRT[Ch3];
		}
		//Lift control
		if(vexRT[Btn6U]){
			lift(MHMotorPowerMax, MHLiftDirectionUp);
		}
		else if(vexRT[Btn6D]){
			lift(MHMotorPowerMax, MHLiftDirectionDown);
		}
		//If neither button is pressed, the lift must be stopped, or it will keep going
		else{
			lift(MHMotorPowerStop, MHLiftDirectionStop);
		}
		//Skyrise Intake
		if(vexRT[Btn8D]){
			SensorValue[skyriseClaw] = MHPneumaticPositionOpen;
		}
		else if(vexRT[Btn8R]){
			SensorValue[skyriseClaw] = MHPneumaticPositionClosed;
		}
		//Skyrise arm rotation
		if(vexRT[Btn7R]){
			motor[skyriseArm] = MHMotorPowerHalf;
		}
		else if(vexRT[Btn7L]){
			motor[skyriseArm] = -MHMotorPowerHalf;
		}
		else{
			motor[skyriseArm] = MHMotorPowerStop;
		}
		//Cube Intake
		if(vexRT[Btn5U]){
			liftCube(MHLiftDirectionUp);
		}
		else if(vexRT[Btn5D]){
			liftCube(MHLiftDirectionDown);
		}
		else{
			liftCube(MHLiftDirectionStop);
		}
	}
}
