MHTeamColor autonSelection;
void pre_auton(){
	bLCDBacklight = true;
	clearLCD();
	displayLCDCenteredString(0, "Autonomous?");
	displayLCDCenteredString(1, "Red    1    Blue");
	while(true){
		if(nLCDButtons == MHLCDButtonLeft){
			autonSelection = MHTeamColorRed;
			break;
		}
		else if(nLCDButtons == MHLCDButtonRight){
			autonSelection = MHTeamColorBlue;
			break;
		}
		else if(nLCDButtons == MHLCDButtonCenter){
			autonSelection = MHTeamColorNone;
			break;
		}
	}
	clearLCD();
	bLCDBacklight = false;
}
task autonomous(){
	switch(autonSelection){
		case MHTeamColorBlue:
			liftForEncoderDistance(1200, MHMotorPowerMax);
			basicDrive(MHMotorPowerMax, MHMotorPowerStop);
			wait1Msec(850);
			stopDrive();
			liftCube(MHMotorPowerMax, MHLiftDirectionDown);
			wait1Msec(MHTimeOneSecond * 3);
			liftCube(MHMotorPowerStop, MHLiftDirectionStop);
			basicDrive(-MHMotorPowerHalf, -MHMotorPowerHalf);
			wait1Msec(MHTimeHalfSecond);
			stopDrive();
			//Auton over
			return;
		case MHTeamColorRed:
			liftForEncoderDistance(1200, MHMotorPowerMax);
			basicDrive(MHMotorPowerStop, MHMotorPowerMax);
			wait1Msec(850);
			stopDrive();
			liftCube(MHMotorPowerMax, MHLiftDirectionDown);
			wait1Msec(MHTimeOneSecond * 3);
			liftCube(MHMotorPowerStop, MHLiftDirectionStop);
			basicDrive(-MHMotorPowerHalf, -MHMotorPowerHalf);
			wait1Msec(MHTimeHalfSecond);
			stopDrive();
			//Auton over
			return;
		default:
			basicDrive(MHMotorPowerMax, MHMotorPowerMax);
			wait1Msec(MHTimeOneSecond);
			basicDrive(MHMotorPowerStop, MHMotorPowerStop);
		}
}
task usercontrol(){
	//Drive control
	bLCDBacklight = true;
	string first;
	string second;
	while(true){
		if(nMotorEncoder[lbLift] < 0){
			resetEncoders();
		}
		if(nLCDButtons != MHLCDButtonNone){
			resetEncoders();
		}
		sprintf(first, "%d", nMotorEncoder[lbLift]);
		sprintf(second, "%d", nMotorEncoder[rbLift]);
		displayLCDCenteredString(0, first);
		displayLCDCenteredString(1, second);
		if(abs(vexRT[Ch2]) <= 30){
			stopDriveSide(MHRobotSideRight);
		}
		else{
			motor[rmDrive] = motor[rbDrive] = vexRT[Ch2];
		}
		if(abs(vexRT[Ch3]) <= 30){
			stopDriveSide(MHRobotSideLeft);
		}
		else{
			motor[lmDrive] = motor[lbDrive] = vexRT[Ch3];
		}
		//Lift control
		if(vexRT[Btn6U]){
			lift(MHMotorPowerMax, MHLiftDirectionUp);
		}
		else if(vexRT[Btn6D]){
			lift(MHMotorPowerMax, MHLiftDirectionDown);
		}
		//If neither button is pressed, the lift must be stopped, or it will keep going
		else{
			lift(MHMotorPowerStop, MHLiftDirectionStop);
		}
		//Skyrise Intake
		if(vexRT[Btn8D]){
			SensorValue[skyriseClaw] = MHPneumaticPositionOpen;
		}
		else if(vexRT[Btn8R]){
			SensorValue[skyriseClaw] = MHPneumaticPositionClosed;
		}
		//Cube Intake
		if(vexRT[Btn5U]){
			liftCube(MHMotorPowerMax, MHLiftDirectionUp);
		}
		else if(vexRT[Btn5D]){
			liftCube(MHMotorPowerMax, MHLiftDirectionDown);
		}
		else{
			liftCube(MHMotorPowerStop, MHLiftDirectionStop);
		}
	}
}
